#Comparision Operator (Relational operator)

x=10
y=20

#1.==(Exact equals to)

print(x==10)
print(x=='10')

#2.!= 
print(x!=10)
print(x!=y)

#3.>

print(x>y)
print(x>5)

#4. >=
print(x>=y)
print(x>=5)



#5.<
print(x<y)
print(x<10)


#6.<=

print(x<=y)
print(x<=10)

